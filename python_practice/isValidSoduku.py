def isValidSudoku(board):
        p = 0
        while p < len(board):
            q = 0
            col_list = {}
            row_list = {}
            col_count = 0
            while q < len(board):

                if board[p][q] != "." and board[p][q] not in row_list:
                    row_list[board[p][q]] = 1
                elif board[p][q] != "." and board[p][q] in row_list:
                    row_list[board[p][q]] += 1


                if board[q][p] != "." and board[q][p] not in col_list:
                    col_list[board[q][p]] = 1
                elif board[q][p] != "." and board[q][p] in col_list:
                    col_list[board[q][p]] += 1
                q += 1

            for r in col_list.values():

                if r > 1:
                    return False
                col_list = {}

            for h in row_list.values():
                if h > 1:
                    return False
                row_list = {}
            p += 1

        for a in [0,3,6]:
            for b in [0,3,6]:
                l = []
                for i in board[a:a+3]:
                    # for h in i:
                    for u in i[b:b+3]:
                        for h in u:
                            if h != ".":
                                l += u
                # print(u)
                # print(l)
